DROP TABLE dq_result_summary; 

CREATE TABLE dq_result_summary 
  ( 
     rule_exec_uuid        VARCHAR2(50), 
     rule_exec_version     INTEGER, 
     rule_exec_time        VARCHAR2(50), 
     rule_uuid             VARCHAR2(50), 
     rule_version          INTEGER, 
     rule_name             VARCHAR2(100), 
     rule_desc             VARCHAR2(500),
     datapod_uuid          VARCHAR2(50), 
     datapod_version       INTEGER, 
     datapod_name          VARCHAR2(100),
     datapod_desc          VARCHAR2(500),  
     attribute_id          VARCHAR2(50) , 
     attribute_name        VARCHAR2(100),
     attribute_desc        VARCHAR2(500),
     pii_flag              VARCHAR2(1),
     cde_flag              VARCHAR2(1),
     null_pass_count       INTEGER, 
     null_fail_count       INTEGER, 
     null_score            DECIMAL(10,4), 
     value_pass_count      INTEGER, 
     value_fail_count      INTEGER, 
     value_score           DECIMAL(10,4),
     range_pass_count      INTEGER, 
     range_fail_count      INTEGER, 
     range_score           DECIMAL(10,4),
     datatype_pass_count   INTEGER, 
     datatype_fail_count   INTEGER, 
     datatype_score        DECIMAL(10,4),
     format_pass_count     INTEGER, 
     format_fail_count     INTEGER, 
     format_score          DECIMAL(10,4),
     length_pass_count     INTEGER, 
     length_fail_count     INTEGER, 
     length_score          DECIMAL(10,4),
     refint_pass_count     INTEGER, 
     refint_fail_count     INTEGER, 
     refint_score    	   DECIMAL(10,4),
     dup_pass_count        INTEGER, 
     dup_fail_count        INTEGER, 
     dup_score             DECIMAL(10,4),
     custom_pass_count     INTEGER, 
     custom_fail_count     INTEGER, 
     custom_score          DECIMAL(10,4),
     domain_pass_count     INTEGER, 
     domain_fail_count     INTEGER, 
     domain_score          DECIMAL(10,4),
     blankspace_pass_count INTEGER, 
     blankspace_fail_count INTEGER, 
     blankspace_score      DECIMAL(10,4),
     expression_pass_count INTEGER, 
     expression_fail_count INTEGER, 
     expression_score      DECIMAL(10,4),
     case_pass_count       INTEGER, 
     case_fail_count       INTEGER, 
     case_score            DECIMAL(10,4),
     total_row_count       INTEGER, 
     total_pass_count      INTEGER, 
     total_fail_count      INTEGER, 
     total_fail_percentage DECIMAL(10,4),
     threshold_type        VARCHAR2(50), 
     threshold_limit       INTEGER, 
     threshold_ind         VARCHAR2(50), 
     score                 DECIMAL(10,4),
     param_info            VARCHAR2(500),
     version               INTEGER
  ); 

CREATE INDEX dq_result_summary_version_index ON dq_result_summary(version);
CREATE INDEX dq_result_summary_rule_datapod_uuid_index ON dq_result_summary(rule_uuid, datapod_uuid);
